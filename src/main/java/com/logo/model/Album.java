package com.logo.model;

public class Album {
	int album_id;
	String album_name;
	String album_publisher;
	String album_date;
	int a_id;
	String album_prof_photo;
	String a_name;
	
	

	public String getA_name() {
		return a_name;
	}

	public void setA_name(String a_name) {
		this.a_name = a_name;
	}

	public int getAlbum_id() {
		return album_id;
	}

	public void setAlbum_id(int album_id) {
		this.album_id = album_id;
	}

	public String getAlbum_name() {
		return album_name;
	}

	public void setAlbum_name(String album_name) {
		this.album_name = album_name;
	}

	public String getAlbum_publisher() {
		return album_publisher;
	}

	public void setAlbum_publisher(String album_publisher) {
		this.album_publisher = album_publisher;
	}

	public String getAlbum_date() {
		return album_date;
	}

	public void setAlbum_date(String album_date) {
		this.album_date = album_date;
	}

	public int getA_id() {
		return a_id;
	}

	public void setA_id(int a_id) {
		this.a_id = a_id;
	}

	public String getAlbum_prof_photo() {
		return album_prof_photo;
	}

	public void setAlbum_prof_photo(String album_prof_photo) {
		this.album_prof_photo = album_prof_photo;
	}

}
